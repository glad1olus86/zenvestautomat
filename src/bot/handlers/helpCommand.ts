import { Bot } from 'grammy';
import { BotContext } from '../bot';

const HELP_TEXT = `<b>📋 Команды Zenvest Bot</b>

<b>📋 Меню:</b>
/menu — единое меню (отчёт, часы, топики и т.д.)

<b>📁 Объекты:</b>
/register — создать новый объект (пошаговый мастер)
/cancel — отменить текущую регистрацию
/list — список всех объектов
/unregister &lt;название&gt; — удалить объект
/rename &lt;новое название&gt; — переименовать объект (из привязанного топика)

<b>🔗 Привязка топиков:</b>
/link — привязать топик к объекту (выбор типа: Отчёты / Чеки)
/unlink — отвязать текущий топик от объекта

<b>📊 Отчёты:</b>
<code>REPORT</code> — структурированный отчёт менеджера
/report — суточный отчёт текущего объекта (из привязанного топика)
/reportall — суточные отчёты всех объектов

<b>⏱ Рабочие часы:</b>
/hours — записать часы (выбор рабочего из списка)
👷 Управление рабочими — в личном чате с ботом

<b>📄 Менеджерские счета:</b>
/managerreport — добавить счёт/фактуру (в личном чате с ботом)

<b>ℹ️ Как начать:</b>
1. /register — создайте объект
2. Зайдите в нужный топик
3. /link — привяжите топик (или через /menu)
4. Готово! Бот начнёт собирать сообщения`;

/**
 * Команда /help — показывает список всех команд бота.
 */
export function setupHelpCommand(bot: Bot<BotContext>): void {
  bot.command('help', async (ctx) => {
    await ctx.reply(HELP_TEXT, { parse_mode: 'HTML' });
  });
}
